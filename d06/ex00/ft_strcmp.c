int	ft_strcmp(char *s1, char *s2)
{
	int c1;
	int c2;

	while (1)
	{
		c1 = *s1++;
		c2 = *s2++;
		if (c1 != c2)
		{
			return (c1 - c2);
		}
		if (c1 == '\0')
		{
			return (0);
		}
	}
}
