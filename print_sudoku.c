#include <unistd.h>

void	print_sudoku(char **grid)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < 9)
	{
		j = 0;
		while (grid[i][j])
		{
			write(1, &grid[i][j], 1);
			if (j != 8)
				write(1, " ", 1);
			j++;
		}
		write(1, "\n", 1);
		i++;
	}
}

int	main(void)
{
	char *grid[] = {
		"914375268",
		"287496153",
		"365812479",
		"846521397",
		"529637814",
		"731984526",
		"153749682",
		"698253741",
		"472168935" 
	};
	print_sudoku(grid);
	return (0);
}
